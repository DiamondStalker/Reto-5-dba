    private void CrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CrearActionPerformed
        // TODO add your handling code here:
        String Nombre = txtnombre.getText();
        int precio = 0;
        int catidad = 0;
        boolean Correcto = false;
        try {
            catidad = (int) cantidad.getValue();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Inserta una un valor para cantidad correcta");
            Correcto = false;
        }
        try {
            precio = Integer.parseInt(txtprecio.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ingresa un valor de precio sin simbolos espacioales");
            Correcto = false;
        }

        String Olor = txtolor.getText();

        Date date = DATE.getDate();
        String dateStr = "";
        try {
            DateFormat dateFormat = new SimpleDateFormat("d/M/yy");
            dateStr = dateFormat.format(date);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Inserta una fecha de vencimineto valido");
            Correcto = false;
        }

        Correcto = Nombre != "" ? true : false;
        Correcto = Olor != "" ? true : false;
        if (Correcto) {
            if (cn.nuevoproducto(Nombre, catidad, precio, Olor, dateStr)) {
                Object Datos[] = new Object[5];
                Datos[0] = Nombre;
                Datos[1] = catidad;
                Datos[2] = precio;
                Datos[3] = Olor;
                Datos[4] = dateStr;
                Model.addRow(Datos);
                Limpiar();
                TextoInfo.setForeground(Color.GREEN);
                ImageIcon icon = new ImageIcon("src\\Images/correcto.png");
                TextoInfo.setIcon(icon);
                TextoInfo.setText("Se inserto el usuario de forma correcta");
            } else {
                TextoInfo.setForeground(Color.BLUE);
                ImageIcon icon = new ImageIcon("src\\Images/info.png");
                TextoInfo.setIcon(icon);
                TextoInfo.setText("Completa todos los campos para poder continuar");
            }
        } else {
            JOptionPane.showMessageDialog(null, "El usuario ya se encuentra insertado");
            TextoInfo.setForeground(Color.RED);
            TextoInfo.setText("EL usuario ya existe");
        }

    }//GEN-LAST:event_CrearActionPerformed

