    public Object[][] cargar_datos() {
        Object[][] Usuarios = cn.Usuarios();

        for (int i = 0; i < Usuarios.length; i++) {
            System.out.println("Nombre --> " + Usuarios[i][0]
                    + "**Cantidad --> " + Usuarios[i][1]
                    + "**Precio --> " + Usuarios[i][2]
                    + "**Olor --> " + Usuarios[i][3]
                    + "**fecha_vencimiento --> " + Usuarios[i][4]);
        }
        return Usuarios;
    }

